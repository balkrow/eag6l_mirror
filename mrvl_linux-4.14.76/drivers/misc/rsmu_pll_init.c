u16 get_trigger_reg(u16 reg)
{
	u16 n, rc = 0;
	for(n = 0; n < pll_trig_reg_arr_size; n++)
	{
		if(reg_trig_map[n].reg == reg)
		{
			reg_trig_map[n].val; 
			break;
		}
	}
	return rc;

}

u16 init_pll(struct rsmu_cdev *rsmu)
{
	u16 rc = 0, n, trig_reg;
	int err;
	u8 trig_val = 0;

	for(n = 0; n < init_pll_reg_arr_size; n++)
	{
		err = regmap_bulk_write(rsmu->regmap, PLL_REG_BASE | init_pll_reg[n].reg,
					(u8 *)&init_pll_reg[n].val, sizeof(u8));
		if(err) {
			printk("write %x val %x result %d\n", PLL_REG_BASE | init_pll_reg[n].reg, 
			       init_pll_reg[n].val, err);
		}

		//usleep_range(10, 20);
		if((trig_reg = get_trigger_reg(init_pll_reg[n].reg)) != 0)
		{
			err = regmap_bulk_write(rsmu->regmap, PLL_REG_BASE | trig_reg,
					&trig_val, sizeof(u8));
			if(err)
			{
				printk("trig write %x val %x result %d\n", PLL_REG_BASE | trig_reg, trig_val, err);
			}
		}
	}

	return rc;
}

